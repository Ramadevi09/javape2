package com.stackroute.pe2;

public class EvenNumberCheck {
    //check given number is even or not
    public static boolean isEven(int number){
        Boolean b=false;


        if(number%2==0){
            b=true;
            //if number is even return true
            return b;

        }
        else  {
            return b;
        }
    }
}
